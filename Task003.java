package HWClass19;

public class Task003 {
    public static void myMethod(int x) {
        System.out.println("myMethod(int x): " + x);
    }

    public static void myMethod(int x, int y) {
        System.out.println("myMethod(int x, int y): " + x + ", " + y);
    }

    public static void myMethod(String x) {
        System.out.println("myMethod(String x): " + x);
    }
}

